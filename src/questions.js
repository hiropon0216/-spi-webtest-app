function buildLogic() {
  const result = [];
  let idx = 0;

  for (let si = 0; si < setBank.length; si += 1) {
    const item = setBank[si];
    idx += 1;
    result.push(q(`nl-${String(idx).padStart(3, "0")}`, "nonverbal-logic", "nonverbal",
      item[0], item[1], item[2], explanation(item[3]), item[4], item[5]));
  }

  for (let pi = 0; pi < logicPropositions.length; pi += 1) {
    const prop = logicPropositions[pi];
    const [choices, answer] = choiceSet(prop.choices[prop.answer], prop.choices.filter((_, idx2) => idx2 !== prop.answer), pi);
    idx += 1;
    result.push(q(`nl-${String(idx).padStart(3, "0")}`, "nonverbal-logic", "nonverbal",
      `${prop.premise}\nこのとき確実に言えるものはどれか。`,
      choices, answer, explanation(prop.exp), "standard", "命題"));
  }

  for (let oi = 0; oi < logicOrders.length; oi += 1) {
    const ord = logicOrders[oi];
    const [choices, answer] = choiceSet(ord.choices[ord.answer], ord.choices.filter((_, idx2) => idx2 !== ord.answer), oi);
    idx += 1;
    result.push(q(`nl-${String(idx).padStart(3, "0")}`, "nonverbal-logic", "nonverbal",
      `${ord.premise}\nこのとき確実に言えるものはどれか。`,
      choices, answer, explanation(ord.exp), "basic", "順序"));
  }

  return result;
}


