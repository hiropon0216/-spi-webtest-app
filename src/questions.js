function gcd(a, b) {
  return b ? gcd(b, a % b) : a;
}

function frac(n, d) {
  const g = gcd(n, d);
  return `${n / g}/${d / g}`;
}

function cleanNumber(value) {
  return Number(value.toFixed(2)).toString();
}

